// Normalize time format to 24-hour range format
export function normalizeTimeSlot(time: string): string {
  const timeMapping: Record<string, string> = {
    // Legacy single AM/PM format → 24h range
    "8:00 AM": "8:00-8:30",
    "8:30 AM": "8:30-9:00",
    "9:00 AM": "9:00-9:30",
    "9:30 AM": "9:30-10:00",
    "10:00 AM": "10:00-10:30",
    "10:30 AM": "10:30-11:00",
    "11:00 AM": "11:00-11:30",
    "11:30 AM": "11:30-12:00",
    "12:00 PM": "12:00-12:30",
    "12:30 PM": "12:30-13:00",
    "1:00 PM": "13:00-13:30",
    "1:30 PM": "13:30-14:00",
    "2:00 PM": "14:00-14:30",
    "2:30 PM": "14:30-15:00",
    "3:00 PM": "15:00-15:30",
    "3:30 PM": "15:30-16:00",
    "4:00 PM": "16:00-16:30",
    "4:30 PM": "16:30-17:00",
    "5:00 PM": "17:00-17:30",
    "5:30 PM": "17:30-18:00",
    "6:00 PM": "18:00-18:30",
    "6:30 PM": "18:30-19:00",
    "7:00 PM": "19:00-19:30",
    "7:30 PM": "19:30-20:00",

    // 12h range format → 24h range format
    "8:00-8:30 AM": "8:00-8:30",
    "8:30-9:00 AM": "8:30-9:00",
    "9:00-9:30 AM": "9:00-9:30",
    "9:30-10:00 AM": "9:30-10:00",
    "10:00-10:30 AM": "10:00-10:30",
    "10:30-11:00 AM": "10:30-11:00",
    "11:00-11:30 AM": "11:00-11:30",
    "11:30 AM-12:00 PM": "11:30-12:00",
    "12:00-12:30 PM": "12:00-12:30",
    "12:30-1:00 PM": "12:30-13:00",
    "1:00-1:30 PM": "13:00-13:30",
    "1:30-2:00 PM": "13:30-14:00",
    "2:00-2:30 PM": "14:00-14:30",
    "2:30-3:00 PM": "14:30-15:00",
    "3:00-3:30 PM": "15:00-15:30",
    "3:30-4:00 PM": "15:30-16:00",
    "4:00-4:30 PM": "16:00-16:30",
    "4:30-5:00 PM": "16:30-17:00",
    "5:00-5:30 PM": "17:00-17:30",
    "5:30-6:00 PM": "17:30-18:00",
    "6:00-6:30 PM": "18:00-18:30",
    "6:30-7:00 PM": "18:30-19:00",
    "7:00-7:30 PM": "19:00-19:30",
    "7:30-8:00 PM": "19:30-20:00",
  };

  return timeMapping[time] || time;
}
